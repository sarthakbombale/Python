# Assiment Operatores in python
x = 10
y = 3

# Addition (+) adds two values together
print(x + y)  # Output: 13

# Subtraction (-) subtracts the right value from the left value
print(x - y)  # Output: 7

# Multiplication (*) multiplies two values
print(x * y)  # Output: 30

# Division (/) divides the left value by the right value (always returns a decimal)
print(x / y)  # Output: 3.3333333333333335

# Floor Division (//) divides and rounds DOWN to the nearest whole number
print(x // y) # Output: 3

# Modulus (%) returns the remainder of a division (10 divided by 3 leaves a remainder of 1)
print(x % y)  # Output: 1

# Exponentiation (**) raises the first number to the power of the second number (10 to the power of 3)
print(x ** y) # Output: 1000



# Aithematic operators in python


# Equal (=) assigns the value on the right to the variable on the left
age = 20  

# Add and Assign (+=) adds a number to the current variable value
age += 5  # Same as: age = age + 5. (age is now 25)

# Subtract and Assign (-=) subtracts a number from the current variable value
age -= 2  # Same as: age = age - 2. (age is now 23)

# Multiply and Assign (*=) multiplies the current variable value
age *= 2  # Same as: age = age * 2. (age is now 46)




# Comparison Operators in python
a = 15
b = 20

# Equal to (==) checks if two values are exactly the same
print(a == b)  # Output: False

# Not equal to (!=) checks if two values are different
print(a != b)  # Output: True

# Greater than (>) checks if the left value is bigger than the right value
print(a > b)   # Output: False

# Less than (<) checks if the left value is smaller than the right value
print(a < b)   # Output: True

# Greater than or equal to (>=)
print(a >= 15) # Output: True

# Less than or equal to (<=)
print(b <= 20) # Output: True

# Logical Operators in python

has_ticket = True
is_adult = False

# 'and' returns True only if BOTH conditions are True
print(has_ticket and is_adult) # Output: False (because is_adult is False)

# 'or' returns True if AT LEAST ONE of the conditions is True
print(has_ticket or is_adult)  # Output: True (because has_ticket is True)

# 'not' reverses the result (turns True to False, or False to True)
print(not has_ticket)          # Output: False

